// A collection of charecters is called Strings in JavaScript.
// Strings are used to store and manipulate text.

// Note: In JavaScript, counting starts from 0, unless specified.


// Let's print the length of a string, using the ".length" property.
// Example 1:
let item = "ENO"; // Eno is three charecters long
console.log(item.length) // Output: 3


// Let's print a particular charecter of a string, using the [] bracket property.
// Example 2:
let yourName = "Kans"; // Kans is four charecters long
console.log(yourName[0]) // Output: K
console.log(yourName[2]) // Output: n